class calcLogic {
    constructor() {

    }
    addition = (parm1, parm2) => {
        return parm1 + parm2;
    }
    subtraction = (parm1, parm2) => {
        return parm1 - parm2;
    }
    multiplication = (parm1, parm2) => {
        return parm1 * parm2;
    }
    division = (parm1, parm2) => {
        return parm1 / parm2;
    }
}